// Helper to get feature by ID
export function getFeature(id) {
  return MARKETPLACE_FEATURES.find(f => f.id === id);
}

// Get features by category
export function getFeaturesByCategory(category) {
  return MARKETPLACE_FEATURES.filter(f => f.category === category);
}

// Get features by industry
export function getFeaturesByIndustry(industry) {
  if (!industry || industry === 'All Industries') {
    return MARKETPLACE_FEATURES;
  }
  return MARKETPLACE_FEATURES.filter(f => f.industry === industry || f.industry === 'All Industries');
}

// Get all unique industries
export function getIndustries() {
  const industries = new Set(['All Industries']);
  MARKETPLACE_FEATURES.forEach(f => {
    if (f.industry) industries.add(f.industry);
  });
  return Array.from(industries);
}

// Get features that require navigation slots
export function getFeaturesRequiringNavSlot() {
  return MARKETPLACE_FEATURES.filter(f => f.requiresNavSlot);
}
